public class AcessandoAtribuindoElementosDeArrays {
    public static void main(String[] args) {
        int[] notas = new int[]{8,0,4,5,6};

        //para acessar tem que por o nome da variavel e a posição que ela se encontra.
        // Da para utilizar variaveis também ao invés dos números inteiros, já que pode ser utilizado
        // o comando scanner para o proprio usuario verificar algo.
        System.out.println(notas[1]);
    }

}
